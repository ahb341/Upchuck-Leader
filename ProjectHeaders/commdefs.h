/*
   standard bit defintions to make things more readable
*/

/* the conditional keeps this from being included more than once */
#ifndef COMMDEFS_H
#define COMMDEFS_H

// Launcher
#define FLAG_UP 0xF1
#define FLAG_DOWN 0xF2
#define FIRE 0xF3
#define TEAM_A 0xF4
#define TEAM_B 0xF5

// Drivetrain
#define STOP 0xD1
#define ROT_CCW 0xD2
#define ROT_CW 0xD3
#define DRIVE_FWD 0xD4
#define DRIVE_REV 0xD5
#define DRIVE_FWD_0 0xD6
#define DRIVE_REV_0 0xD7

#endif  /* COMMDEFS_H */

